.MODEL SMALL
.STACK 100H
.DATA
 
 SMG DB 'ENTER INPUT:$'
 SMG1 DB 'IT IS NUMBER$'
 SMG2 DB 'IT IS CAPITAL LETTER$'
 SMG3 DB 'IT IS SMALL LETTER$'
 SMG4 DB 'IT IS SPECIAL CHARACTER$'
 
.CODE
 MAIN PROC
  
  MOV AX,@DATA
  MOV DS,AX
  
  LEA DX,SMG   ;PRINT SMG
  MOV AH,9
  INT 21H
  
  MOV AH,1     ;TAKE INPUT
  INT 21H
  
  MOV BL,AL    ;MOVE 
  
  MOV DX,10    ;NEW LINE
  MOV AH,2
  INT 21H
  MOV DX,13
  MOV AH,2
  INT 21H
         
  LEVEL1:     ;LAVEL NAME
  CMP BL,48   ;COMPARE BL AND 48
  JL LEVEL2   ;JUMP IF BL LESS THEN 48
  JMP LEVEL3  ;JUST JUMP
  
  LEVEL2:
  LEA DX,SMG4
  MOV AH,9
  INT 21H
  
  MOV AH,4CH
  INT 21H
  
  LEVEL3:
  CMP BL,57   
  JG LEVEL4
  JMP LEVEL5
  
  LEVEL4:
  CMP BL,65
  JL LEVEL2
  JMP LEVEL6
  
  LEVEL5:
  LEA DX,SMG1
  MOV AH,9
  INT 21H 
  
  MOV AH,4CH
  INT 21H 
  
  LEVEL6:
  CMP BL,90
  JG LEVEL7
  JMP LEVEL8
  
  LEVEL7:
  CMP BL,97   
  JL LEVEL2   
  JMP LEVEL9
  
  LEVEL8:
  LEA DX,SMG2
  MOV AH,9
  INT 21H
  
  MOV AH,4CH
  INT 21H  
  
  LEVEL9:
  CMP BL,122   
  JG LEVEL2   
  JMP LEVEL10
  
  LEVEL10:
  LEA DX,SMG3
  MOV AH,9
  INT 21H
  
  MOV AH,4CH
  INT 21H 
         
  MAIN ENDP
 END MAIN